/*
	比较运算符 ： ==（相当于数学中的=）

	说明：1.比较运算符的结果为boolean类型

*/

public class EqualsTest {
    public static void main(String[] args) {

        boolean boo = false;

        if (boo == true){ //= true
            System.out.println("true");
        }else {
            System.out.println("false");
        }

    }
}
